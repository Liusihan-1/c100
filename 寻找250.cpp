//输入格式：
//输入在一行中给出不知道多少个绝对值不超过1000的整数，其中保证至少存在一个“250”。
//输出格式：
//在一行中输出第一次出现的“250”是对方扔过来的第几个数字（计数从1开始）。题目保证输出的数字在整型范围内。
#include<stdio.h>
#include<stdlib.h>
int main()
{
    int i,n,m;
    for(i=1;;i++)
    {
        scanf("%d",&n);
        if(n==250)
        break;
    }
    m=i;
    printf("%d",m);
}
